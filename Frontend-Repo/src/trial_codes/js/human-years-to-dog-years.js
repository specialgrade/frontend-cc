// convert human years to dog years

let myAge = 18; // my current age 
let earlyYears = 2; // first two years
earlyYears *= 10.5;
laterYears = myAge - 2; // current age - first two years
laterYears *= 4; // dog years
console.log(`Early Years: ${earlyYears}`);
console.log(`Later Years: ${laterYears}`);
console.log('- - - - - - - - - - - - -')
myAgeInDogYears = earlyYears + laterYears; // my current age in dog years 
let myName = 'Aly';
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`);